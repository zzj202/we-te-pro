function parseLotteryData(input) {
    const lines = input.split('\n').filter(line => line.trim() !== '');
    const results = [];
    let currentDate = '';
    let currentNumbers = [];

    let index = 0
    for (const line of lines) {
        // 检查是否是日期行，如 "2025年09月15日 第258期"
        const dateMatch = line.match(/(\d{4}年\d{2}月\d{2}日)/);
        if (dateMatch) {
            // 如果已经有收集的数字，先保存前一条记录
            if (currentNumbers.length > 0) {
                results.push({
                    id: index++,
                    date: currentDate,
                    numbers: [...currentNumbers]
                });
                currentNumbers = [];
            }

            currentDate = dateMatch[0];
            continue;
        }

        // 处理数字行，保留为字符串格式
        const numStr = line.trim();
        if (numStr && !isNaN(parseInt(numStr, 10))) {
            currentNumbers.push(numStr);
        }
    }

    // 添加最后一条记录
    if (currentNumbers.length > 0) {
        results.push({
            date: currentDate,
            numers: [...currentNumbers]
        });
    }

    return results;
}
