function parseSimpleLotteryData(input) {
    const lines = input.split('\n').filter(line => line.trim() !== '');
    const results = [];
    let currentRecord = null;
    let idCounter = Math.floor(lines.length / 9); // 根据数据行数估算ID

    for (const line of lines) {
        // 检查是否是期数行，如 "101期(开奖时间:2025-09-16)"
        const periodMatch = line.match(/(\d+)期\(开奖时间:(\d{4}-\d{2}-\d{2})\)/);
        if (periodMatch) {
            // 如果已经有收集的记录，先保存前一条记录
            if (currentRecord) {
                results.push(currentRecord);
            }
            currentRecord = {
                id: idCounter--,
                date: periodMatch[2], // 使用开奖时间作为date
                numbers: [] // 纯数字数组
            };
            continue;
        }

        // 只处理数字行，忽略生肖行和特码标记
        const numMatch = line.match(/^(\d+)$/);
        if (numMatch && currentRecord) {
            currentRecord.numbers.push(numMatch[1]);
        }
    }

    // 添加最后一条记录
    if (currentRecord) {
        results.push(currentRecord);
    }

    return results;
}

