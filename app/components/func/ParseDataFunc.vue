function parseLotteryDataWithDate(input) {
    const lines = input.split('\n').filter(line => line.trim() !== '');
    const results = [];
    let currentRecord = null;

    // 获取当前日期
    const yesterday = new Date();
    yesterday.setDate(yesterday.getDate() - 1);
    let currentDate = new Date(yesterday); // 从昨天开始

    for (const line of lines) {
        // 检查是否是期数行
        const periodMatch = line.match(/第\s*(\d+)\s*期开奖结果/);
        if (periodMatch) {
            if (currentRecord) {
                results.push(currentRecord);
                // 每处理一条记录，日期往前推一天
                currentDate.setDate(currentDate.getDate() - 1);
            }

            currentRecord = {
                id: parseInt(periodMatch[1]),
                date: formatDate(currentDate),
                numbers: []
            };
            continue;
        }

        // 处理数字行
        const numMatch = line.match(/^(\d+)$/);
        if (numMatch && currentRecord) {
            currentRecord.numbers.push(numMatch[1]);
        }

        // 处理带+号的特殊号码行
        const specialMatch = line.match(/^\+\s*(\d+)$/);
        if (specialMatch && currentRecord) {
            currentRecord.numbers.push(specialMatch[1]);
        }
    }

    if (currentRecord) {
        results.push(currentRecord);
    }

    return results;
}

// 格式化日期为 YYYY-MM-DD
function formatDate(date) {
    const year = date.getFullYear();
    const month = String(date.getMonth() + 1).padStart(2, '0');
    const day = String(date.getDate()).padStart(2, '0');
    return `${year}-${month}-${day}`;
}
